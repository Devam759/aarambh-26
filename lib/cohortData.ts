// Pre-calculate normalized mapping to support registration formats with/without dots, slashes, or dashes.
const NORMALIZED_REG_TO_COHORT: Record<string, string> = {};
for (const [key, val] of Object.entries(REGISTRATION_TO_COHORT)) {
  const normalizedKey = key.toUpperCase().replace(/[^A-Z0-9]/g, '');
  NORMALIZED_REG_TO_COHORT[normalizedKey] = val;
}

/**
 * Returns the cohort name for a given registration number.
 */
export function getStudentCohort(regNum: string): string | null {
  if (!regNum) return null;
  const clean = regNum.toUpperCase().replace(/[^A-Z0-9]/g, '');
  return NORMALIZED_REG_TO_COHORT[clean] || null;
}

/**
 * Returns leader details for a given cohort.
 */
export function getCohortLeaderDetails(cohort: string) {
  if (!cohort) return null;
  const leaderName = COHORT_TO_LEADER[cohort.toUpperCase()];
  if (!leaderName) return null;
  return {
    name: leaderName,
    phone: LEADER_PHONES[leaderName] || '[Phone Number Not Found]'
  };
}

/**
 * Resolves the batch info for a given cohort name.
 */
export function getBatchForCohort(cohort: string): { batchName: string; pdfFileName: string } {
  if (!cohort) {
    return { batchName: 'Batch 1', pdfFileName: 'batch_1_schedule.pdf' };
  }
  const firstLetter = cohort.charAt(0).toUpperCase();
  
  // Batch mappings based on Cluster
  if (['A', 'E', 'I'].includes(firstLetter)) {
    return { batchName: 'Batch 1', pdfFileName: 'batch_1_schedule.pdf' };
  } else if (['B', 'F', 'J'].includes(firstLetter)) {
    return { batchName: 'Batch 2', pdfFileName: 'batch_2_schedule.pdf' };
  } else if (['C', 'G', 'K'].includes(firstLetter)) {
    return { batchName: 'Batch 3', pdfFileName: 'batch_3_schedule.pdf' };
  } else if (['D', 'H', 'L'].includes(firstLetter)) {
    return { batchName: 'Batch 4', pdfFileName: 'batch_4_schedule.pdf' };
  }
  
  return { batchName: 'Batch 1', pdfFileName: 'batch_1_schedule.pdf' };
}
